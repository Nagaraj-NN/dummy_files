    materialized='incremental',
    unique_key='BDY_PRT_KEY',
    incremental_strategy='merge',
    full_refresh=false,
    pre_hook=[
        log_model_start(this, 'S_M_SHEMS_BDY_PRT')
    ],
    post_hook=[
        log_model_end(this, 'S_M_SHEMS_BDY_PRT')
    ]
) }}

WITH
JOB_PARAM AS (
    SELECT PARAM_ID, PARAM_NAME, PARAM_VALUE FROM {{source('CRPDB01_METADATA','SHMSADM_JOB_PARAMETERS')}}
    WHERE JOB_NAME= 'SHEMS_BDY_PRT_DIM' AND AUTOSYS_JOB_NAME = 'S_M_SHEMS_BDY_PRT' AND ACTIVE_IND = 'Y'
),

JOB_CONTROL AS (
    SELECT JOB_ID, START_DATE, END_DATE FROM {{source('CRPDB01_METADATA','SHMSADM_JOB_CONTROL')}}
    WHERE JOB_NAME= 'SHEMS_BDY_PRT_DIM' AND AUTOSYS_JOB_NAME = 'S_M_SHEMS_BDY_PRT'
),
SQ_ODS_SHMS_INCDNT_BDY_PRT AS (
  SELECT
      B.BDY_PRT_ID AS BDY_PRT_ID,
      A.INJRY_ILLNSS_ID AS INJRY_ILLNSS_ID,
      B.CREATED_DT AS CREATED_DT,
      B.MODIFIED_DT AS MODIFIED_DT,
      B.RECORD_NM AS RECORD_NM,
      B.RECORD_NB AS RECORD_NB,
      B.BDY_PRT_NM AS BDY_PRT_NM,
      B.INJ_NTR_TX AS INJ_NTR_TX,
      C.HIGH_ENRGY_HZRD_TX AS HIGH_ENRGY_HZRD_TX,
      C.INJRY_SUSTAIN_1_TX AS INJRY_SUSTAIN_1_TX,
      C.INJRY_SUSTAIN_2_TX AS INJRY_SUSTAIN_2_TX,
      C.BDY_PRT_GRP_TX AS BDY_PRT_GRP_TX
    FROM {{source('CRPDB01_SHMSADM','ODS_SHMS_INCDNT_OII_BDYPRT')}} A  --CRPDB01_DEV_SANDBOX.SHMSADM.ODS_SHMS_INCDNT_OII_BDYPRT
    LEFT JOIN {{source('CRPDB01_SHMSADM','ODS_SHMS_INCDNT_BDY_PRT')}} B  --CRPDB01_DEV_SANDBOX.SHMSADM.ODS_SHMS_INCDNT_BDY_PRT
      ON A.BPIN_ID = B.BDY_PRT_ID
    LEFT JOIN {{source('CRPDB01_SHMSADM','ODS_SHMS_INCDNT')}} C  --CRPDB01_DEV_SANDBOX.SHMSADM.ODS_SHMS_INCDNT
      ON A.INJRY_ILLNSS_ID = C.INCDNT_ID
    WHERE B.BDY_PRT_ID IS NOT NULL
      AND A.INJRY_ILLNSS_ID IS NOT NULL
),
CURRENT_DIM AS (
  SELECT
      DIM.BDY_PRT_KEY,
      DIM.BDY_PRT_ID,
      DIM.INJRY_ILLNSS_ID,
      DIM.CREATED_DT,
      DIM.MODIFIED_DT,
      DIM.RECORD_NM,
      DIM.RECORD_NB,
      DIM.BDY_PRT_NM,
      DIM.INJ_NTR_TX,
      DIM.EFFCT_STRT_DT,
      DIM.EFFCT_END_DT,
      DIM.ACTIVE_FLAG,
      DIM.HIGH_ENRGY_HZRD_TX,
      DIM.INJRY_SUSTAIN_1_TX,
      DIM.INJRY_SUSTAIN_2_TX,
      DIM.BDY_PRT_GRP_TX
    FROM {{this}} DIM  --CRPDB01_DEV_SANDBOX.SHMSADM.SHEMS_BDY_PRT_DIM
    WHERE DIM.ACTIVE_FLAG = 'Y'
),
EXPTRANS_step1 AS (
  SELECT
      SRC.BDY_PRT_ID,
      SRC.INJRY_ILLNSS_ID,
      SRC.CREATED_DT,
      SRC.MODIFIED_DT,
      SRC.RECORD_NM,
      SRC.RECORD_NB,
      SRC.BDY_PRT_NM,
      SRC.INJ_NTR_TX,
      SRC.HIGH_ENRGY_HZRD_TX,
      SRC.INJRY_SUSTAIN_1_TX,
      SRC.INJRY_SUSTAIN_2_TX,
      SRC.BDY_PRT_GRP_TX,
      DIM.BDY_PRT_KEY AS LKP_BDY_PRT_KEY,
      DIM.BDY_PRT_ID AS LKP_BDY_PRT_ID,
      DIM.INJRY_ILLNSS_ID AS LKP_INJRY_ILLNSS_ID,
      DIM.CREATED_DT AS LKP_CREATED_DT,
      DIM.MODIFIED_DT AS LKP_MODIFIED_DT,
      DIM.RECORD_NM AS LKP_RECORD_NM,
      DIM.RECORD_NB AS LKP_RECORD_NB,
      DIM.BDY_PRT_NM AS LKP_BDY_PRT_NM,
      DIM.INJ_NTR_TX AS LKP_INJ_NTR_TX,
      DIM.HIGH_ENRGY_HZRD_TX AS LKP_HIGH_ENRGY_HZRD_TX,
      DIM.INJRY_SUSTAIN_1_TX AS LKP_INJRY_SUSTAIN_1_TX,
      DIM.INJRY_SUSTAIN_2_TX AS LKP_INJRY_SUSTAIN_2_TX,
      DIM.BDY_PRT_GRP_TX AS LKP_BDY_PRT_GRP_TX,
      MD5(
        COALESCE(CAST(IFF(SRC.CREATED_DT IS NULL, '', TO_VARCHAR(SRC.CREATED_DT, 'MMDDYYYY')) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.MODIFIED_DT IS NULL, '', TO_VARCHAR(SRC.MODIFIED_DT, 'MMDDYYYY')) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.RECORD_NM IS NULL, '', SRC.RECORD_NM) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.RECORD_NB IS NULL, '0', SRC.RECORD_NB) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.BDY_PRT_NM IS NULL, '', SRC.BDY_PRT_NM) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.INJ_NTR_TX IS NULL, '', SRC.INJ_NTR_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.HIGH_ENRGY_HZRD_TX IS NULL, '', SRC.HIGH_ENRGY_HZRD_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.INJRY_SUSTAIN_1_TX IS NULL, '', SRC.INJRY_SUSTAIN_1_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.INJRY_SUSTAIN_2_TX IS NULL, '', SRC.INJRY_SUSTAIN_2_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(SRC.BDY_PRT_GRP_TX IS NULL, '', SRC.BDY_PRT_GRP_TX) AS VARCHAR), '')
      ) AS SRC_CHECKSUM,
      MD5(
        COALESCE(CAST(IFF(DIM.CREATED_DT IS NULL, '', TO_VARCHAR(DIM.CREATED_DT, 'MMDDYYYY')) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.MODIFIED_DT IS NULL, '', TO_VARCHAR(DIM.MODIFIED_DT, 'MMDDYYYY')) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.RECORD_NM IS NULL, '', DIM.RECORD_NM) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.RECORD_NB IS NULL, '0', DIM.RECORD_NB) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.BDY_PRT_NM IS NULL, '', DIM.BDY_PRT_NM) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.INJ_NTR_TX IS NULL, '', DIM.INJ_NTR_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.HIGH_ENRGY_HZRD_TX IS NULL, '', DIM.HIGH_ENRGY_HZRD_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.INJRY_SUSTAIN_1_TX IS NULL, '', DIM.INJRY_SUSTAIN_1_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.INJRY_SUSTAIN_2_TX IS NULL, '', DIM.INJRY_SUSTAIN_2_TX) AS VARCHAR), '') ||
        COALESCE(CAST(IFF(DIM.BDY_PRT_GRP_TX IS NULL, '', DIM.BDY_PRT_GRP_TX) AS VARCHAR), '')
      ) AS LKP_CHECKSUM
    FROM SQ_ODS_SHMS_INCDNT_BDY_PRT SRC
    LEFT JOIN CURRENT_DIM DIM
      ON DIM.BDY_PRT_ID = SRC.BDY_PRT_ID
     AND DIM.INJRY_ILLNSS_ID = SRC.INJRY_ILLNSS_ID
),
EXPTRANS AS (
  SELECT
      EXPTRANS_step1.*,
      IFF(
        LKP_BDY_PRT_ID IS NULL OR LKP_INJRY_ILLNSS_ID IS NULL,
        'I',
        IFF(SRC_CHECKSUM <> LKP_CHECKSUM, 'U', NULL)
      ) AS FLAG
    FROM EXPTRANS_step1
),
CHANGED_ROWS AS (
  SELECT
      *
    FROM EXPTRANS
    WHERE FLAG IN ('I', 'U')
),
EXISTING_ROWS_TO_EXPIRE AS (
  SELECT
      LKP_BDY_PRT_KEY AS BDY_PRT_KEY,
      BDY_PRT_ID,
      INJRY_ILLNSS_ID,
      CREATED_DT,
      MODIFIED_DT,
      RECORD_NM,
      RECORD_NB,
      BDY_PRT_NM,
      INJ_NTR_TX,
      CURRENT_TIMESTAMP() AS EFFCT_STRT_DT,
      CURRENT_TIMESTAMP() AS EFFCT_END_DT,
      'N' AS ACTIVE_FLAG,
      'SHMSUSER' AS EDW_LAST_UPDT_USER_ID,
      CURRENT_TIMESTAMP() AS EDW_LAST_UPDT_TS,
      HIGH_ENRGY_HZRD_TX,
      INJRY_SUSTAIN_1_TX,
      INJRY_SUSTAIN_2_TX,
      BDY_PRT_GRP_TX
    FROM CHANGED_ROWS
    WHERE FLAG = 'U'
),
NEW_ACTIVE_ROWS AS (
  SELECT
      COALESCE((SELECT MAX(BDY_PRT_KEY::NUMBER) FROM {{this}}), 0)  --CRPDB01_DEV_SANDBOX.SHMSADM.SHEMS_BDY_PRT_DIM
        + ROW_NUMBER() OVER (ORDER BY BDY_PRT_ID, INJRY_ILLNSS_ID) AS BDY_PRT_KEY,
      BDY_PRT_ID,
      INJRY_ILLNSS_ID,
      CREATED_DT,
      MODIFIED_DT,
      RECORD_NM,
      RECORD_NB,
      BDY_PRT_NM,
      INJ_NTR_TX,
      CURRENT_TIMESTAMP() AS EFFCT_STRT_DT,
      TRY_TO_DATE('12-31-9999', 'MM-DD-YYYY') AS EFFCT_END_DT,
      'Y' AS ACTIVE_FLAG,
      'SHMSUSER' AS EDW_LAST_UPDT_USER_ID,
      CURRENT_TIMESTAMP() AS EDW_LAST_UPDT_TS,
      HIGH_ENRGY_HZRD_TX,
      INJRY_SUSTAIN_1_TX,
      INJRY_SUSTAIN_2_TX,
      BDY_PRT_GRP_TX
    FROM CHANGED_ROWS
),
FINAL_ROWS AS (
  SELECT
      BDY_PRT_KEY,
      BDY_PRT_ID,
      INJRY_ILLNSS_ID,
      CREATED_DT,
      MODIFIED_DT,
      RECORD_NM,
      RECORD_NB,
      BDY_PRT_NM,
      INJ_NTR_TX,
      EFFCT_STRT_DT,
      EFFCT_END_DT,
      ACTIVE_FLAG,
      EDW_LAST_UPDT_USER_ID,
      EDW_LAST_UPDT_TS,
      HIGH_ENRGY_HZRD_TX,
      INJRY_SUSTAIN_1_TX,
      INJRY_SUSTAIN_2_TX,
      BDY_PRT_GRP_TX
    FROM EXISTING_ROWS_TO_EXPIRE
    UNION ALL
    SELECT
      BDY_PRT_KEY,
      BDY_PRT_ID,
      INJRY_ILLNSS_ID,
      CREATED_DT,
      MODIFIED_DT,
      RECORD_NM,
      RECORD_NB,
      BDY_PRT_NM,
      INJ_NTR_TX,
      EFFCT_STRT_DT,
      EFFCT_END_DT,
      ACTIVE_FLAG,
      EDW_LAST_UPDT_USER_ID,
      EDW_LAST_UPDT_TS,
      HIGH_ENRGY_HZRD_TX,
      INJRY_SUSTAIN_1_TX,
      INJRY_SUSTAIN_2_TX,
      BDY_PRT_GRP_TX
    FROM NEW_ACTIVE_ROWS
)

SELECT
    CAST(SRC.BDY_PRT_KEY AS NUMBER(10,0)) AS BDY_PRT_KEY,
    CAST(LEFT(TRIM(COALESCE(TGT.BDY_PRT_ID, SRC.BDY_PRT_ID)), 200) AS VARCHAR(200)) AS BDY_PRT_ID,
    CAST(LEFT(TRIM(COALESCE(TGT.INJRY_ILLNSS_ID, SRC.INJRY_ILLNSS_ID)), 200) AS VARCHAR(200)) AS INJRY_ILLNSS_ID,
    CAST(COALESCE(TGT.CREATED_DT, SRC.CREATED_DT) AS TIMESTAMP_NTZ(9)) AS CREATED_DT,
    CAST(COALESCE(TGT.MODIFIED_DT, SRC.MODIFIED_DT) AS TIMESTAMP_NTZ(9)) AS MODIFIED_DT,
    CAST(LEFT(TRIM(COALESCE(TGT.RECORD_NM, SRC.RECORD_NM)), 200) AS VARCHAR(200)) AS RECORD_NM,
    CAST(LEFT(TRIM(COALESCE(TGT.RECORD_NB, SRC.RECORD_NB)), 100) AS VARCHAR(100)) AS RECORD_NB,
    CAST(LEFT(TRIM(COALESCE(TGT.BDY_PRT_NM, SRC.BDY_PRT_NM)), 200) AS VARCHAR(200)) AS BDY_PRT_NM,
    CAST(LEFT(TRIM(COALESCE(TGT.INJ_NTR_TX, SRC.INJ_NTR_TX)), 200) AS VARCHAR(200)) AS INJ_NTR_TX,
    CAST(COALESCE(TGT.EFFCT_STRT_DT, SRC.EFFCT_STRT_DT) AS TIMESTAMP_NTZ(9)) AS EFFCT_STRT_DT,
    CAST(SRC.EFFCT_END_DT AS TIMESTAMP_NTZ(9)) AS EFFCT_END_DT,
    CAST(LEFT(TRIM(SRC.ACTIVE_FLAG), 1) AS VARCHAR(1)) AS ACTIVE_FLAG,
    CAST(LEFT(TRIM(SRC.EDW_LAST_UPDT_USER_ID), 10) AS VARCHAR(10)) AS EDW_LAST_UPDT_USER_ID,
    CAST(SRC.EDW_LAST_UPDT_TS AS TIMESTAMP_NTZ(6)) AS EDW_LAST_UPDT_TS,
    CAST(LEFT(TRIM(COALESCE(TGT.HIGH_ENRGY_HZRD_TX, SRC.HIGH_ENRGY_HZRD_TX)), 200) AS VARCHAR(200)) AS HIGH_ENRGY_HZRD_TX,
    CAST(LEFT(TRIM(COALESCE(TGT.INJRY_SUSTAIN_1_TX, SRC.INJRY_SUSTAIN_1_TX)), 200) AS VARCHAR(200)) AS INJRY_SUSTAIN_1_TX,
    CAST(LEFT(TRIM(COALESCE(TGT.INJRY_SUSTAIN_2_TX, SRC.INJRY_SUSTAIN_2_TX)), 200) AS VARCHAR(200)) AS INJRY_SUSTAIN_2_TX,
    CAST(LEFT(TRIM(COALESCE(TGT.BDY_PRT_GRP_TX, SRC.BDY_PRT_GRP_TX)), 200) AS VARCHAR(200)) AS BDY_PRT_GRP_TX

FROM
    FINAL_ROWS SRC
LEFT JOIN
    {{this}} TGT
ON
    SRC.BDY_PRT_KEY = TGT.BDY_PRT_KEY
WHERE
  (
   DECODE(CAST(tgt.EFFCT_END_DT AS TIMESTAMP_NTZ(9)), CAST(src.EFFCT_END_DT AS TIMESTAMP_NTZ(9)), 1, 0) = 0 OR
   DECODE(CAST(LEFT(TRIM(tgt.ACTIVE_FLAG), 1) AS VARCHAR(1)), CAST(LEFT(TRIM(src.ACTIVE_FLAG), 1) AS VARCHAR(1)), 1, 0) = 0 OR
   DECODE(CAST(LEFT(TRIM(tgt.EDW_LAST_UPDT_USER_ID), 10) AS VARCHAR(10)), CAST(LEFT(TRIM(src.EDW_LAST_UPDT_USER_ID), 10) AS VARCHAR(10)), 1, 0) = 0
   --DECODE(CAST(tgt.EDW_LAST_UPDT_TS AS TIMESTAMP_NTZ(6)), CAST(src.EDW_LAST_UPDT_TS AS TIMESTAMP_NTZ(6)), 1, 0) = 0  -- AUDIT COLUMN, MAY NOT BE NEEDED - PLEASE VERIFY
  )
